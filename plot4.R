data<- read.table("household_power_consumption.txt",header=TRUE,sep=";",stringsAsFactors = FALSE,dec=".")
subsetdata <- data[data$Date %in% c("1/2/2007","2/2/2007"),]
gap<- as.numeric(subsetdata$Global_active_power)
vol <- as.numeric(subsetdata$Voltage)
grp <- as.numeric(subsetdata$Global_reactive_power)
sm1 <- as.numeric(subsetdata$Sub_metering_1)
sm2 <- as.numeric(subsetdata$Sub_metering_2)
sm3 <- as.numeric(subsetdata$Sub_metering_3)
datetime <- strptime(paste(subsetdata$Date, subsetdata$Time, sep=" "), "%d/%m/%Y %H:%M:%S") 
png("plot4.png",width = 480,height = 480,units = "px")
par(mfrow = c(2,2), oma= c(0,1,0,0))
plot(datetime,gap,type = "l",xlab = "",ylab = "Global Active Power")
plot(datetime,vol,type= "l",xlab ="", ylab = "voltage")
plot(datetime, sm1, type="l", ylab="Energy Submetering", xlab="")
lines(datetime, sm2, type="l", col="red")
lines(datetime, sm3, type="l", col="blue")
legend("topright", c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"), lty=1, lwd=2.5, col=c("black", "red", "blue"))
plot(datetime,grp,type="l",xlab ="", ylab = "Global Reactive Power")
dev.off()

